# Numeric
a = 10
b = 10.10
print(type(a))
print(a)
print(b)

# Sequence
c = "Hi"
d = [1,2,3,3.5,4,"halllo"]
e = (1,2,2,34,5)
print(c)
print(d)
print(e)

# Set
f = set(e)
g = frozenset(e)
print(f)
print(type(f))
print(g)
print(type(g))

# Mapping
dict = {'name':"Atharv",'age':17}
print(dict)

# Boolean
n = 4
if n%2==0:
    print(True)
else: 
    print(False)
print(type(True))

# Null
f = None
print(f)
print(type(f))